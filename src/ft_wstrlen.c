#include "ft_printf.h"

ssize_t		ft_wstrlen(const wchar_t *s)
{
	ssize_t		i;

	if (!s)
		return (0);
	i = 0;
	while (*s != '\0')
	{
		if (*s < 127)
			i++;
		else if (*s < 2047)
			i += 2;
		else if (*s < 65535)
			i += 3;
		else
			i += 4;
		s++;
	}
	return (i);
}